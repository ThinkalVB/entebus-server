EXECUTIVE_PICTURES = "executive-pictures"  # Stores profile pictures of executives
OPERATOR_PICTURES = "operator-pictures"  # Stores profile pictures of operators
VENDOR_PICTURES = "vendor-pictures"  # Stores profile pictures of vendors
